package Projeto;
public class Livro {
    int id;
    String titulo;
    String autor;
    int ano_publicacao;

    public Livro(int id, String titulo, String autor, int ano_publicacao) {
        this.id = id;
        this.titulo = titulo;
        this.autor = autor;
        this.ano_publicacao = ano_publicacao;
    }
}
